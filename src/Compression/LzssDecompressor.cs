namespace StuffItReader.Compression;

/// <summary>
/// LZSS decompressor for StuffIt compression method 13.
/// Ported from XADStuffIt13Handle.m
/// </summary>
internal sealed class LzssDecompressor
{
    private const int WindowSize = 65536;
    
    private readonly Stream _input;
    private readonly byte[] _window;
    private int _windowPosition;
    
    private PrefixCode? _firstCode;
    private PrefixCode? _secondCode;
    private PrefixCode? _offsetCode;
    private PrefixCode? _currentCode;
    private HuffmanBitReader? _bitReader;

    public LzssDecompressor(Stream input)
    {
        _input = input;
        _window = new byte[WindowSize];
        _windowPosition = 0;
    }

    public void Decompress(Stream output, long decompressedLength)
    {
        _bitReader = new HuffmanBitReader(_input);
        ResetLZSS();

        long outputCount = 0;
        while (outputCount < decompressedLength)
        {
            int val = NextLiteralOrMatch(out int offset, out int length);

            if (val < 0x100)
            {
                // Literal byte
                byte b = (byte)val;
                output.WriteByte(b);
                _window[_windowPosition] = b;
                _windowPosition = (_windowPosition + 1) % WindowSize;
                outputCount++;
            }
            else if (val == -1)
            {
                // End of stream
                break;
            }
            else
            {
                // Match - copy from window
                int copyPos = (_windowPosition - offset + WindowSize) % WindowSize;
                for (int i = 0; i < length; i++)
                {
                    byte b = _window[copyPos];
                    output.WriteByte(b);
                    _window[_windowPosition] = b;
                    _windowPosition = (_windowPosition + 1) % WindowSize;
                    copyPos = (copyPos + 1) % WindowSize;
                    outputCount++;
                    
                    if (outputCount >= decompressedLength)
                        break;
                }
            }
        }
    }

    private void ResetLZSS()
    {
        int val = _input.ReadByte();
        if (val == -1)
        {
            throw new EndOfStreamException("Unexpected end of stream in LZSS header");
        }

        int code = val >> 4;

        if (code == 0)
        {
            // Dynamic Huffman codes
            PrefixCode metaCode = CreateMetaCode();

            _firstCode = AllocAndParseCode(321, metaCode);
            if ((val & 0x08) != 0)
            {
                _secondCode = _firstCode;
            }
            else
            {
                _secondCode = AllocAndParseCode(321, metaCode);
            }
            _offsetCode = AllocAndParseCode((val & 0x07) + 10, metaCode);
        }
        else if (code < 6)
        {
            // Predefined Huffman codes
            _firstCode = PrefixCode.FromLengths(FirstCodeLengths[code - 1], 321, 32);
            _secondCode = PrefixCode.FromLengths(SecondCodeLengths[code - 1], 321, 32);
            _offsetCode = PrefixCode.FromLengths(OffsetCodeLengths[code - 1], OffsetCodeSize[code - 1], 32);
        }
        else
        {
            throw new InvalidDataException($"Invalid LZSS code type: {code}");
        }

        _currentCode = _firstCode;
    }

    private PrefixCode CreateMetaCode()
    {
        int[] metaCodes = new int[]
        {
            0x5d8, 0x058, 0x040, 0x0c0, 0x000, 0x078, 0x02b, 0x014,
            0x00c, 0x01c, 0x01b, 0x00b, 0x010, 0x020, 0x038, 0x018,
            0x0d8, 0xbd8, 0x180, 0x680, 0x380, 0xf80, 0x780, 0x480,
            0x080, 0x280, 0x3d8, 0xfd8, 0x7d8, 0x9d8, 0x1d8, 0x004,
            0x001, 0x002, 0x007, 0x003, 0x008
        };

        int[] metaCodeLengths = new int[]
        {
            11, 8, 8, 8, 8, 7, 6, 5, 5, 5, 5, 6, 5, 6, 7, 7, 9, 12, 10, 11, 11, 12,
            12, 11, 11, 11, 12, 12, 12, 12, 12, 5, 2, 2, 3, 4, 5
        };

        int[] values = new int[37];
        for (int i = 0; i < 37; i++)
        {
            values[i] = i;
        }

        return PrefixCode.FromExplicitCodes(metaCodes, metaCodeLengths, values);
    }

    private PrefixCode AllocAndParseCode(int numCodes, PrefixCode metaCode)
    {
        int length = 0;
        int[] lengths = new int[numCodes];

        for (int i = 0; i < numCodes; i++)
        {
            int val = metaCode.DecodeSymbol(_bitReader!);

            switch (val)
            {
                case 31:
                    length = -1;
                    break;
                case 32:
                    length++;
                    break;
                case 33:
                    length--;
                    break;
                case 34:
                    if (_bitReader!.ReadBit() != 0)
                    {
                        lengths[i++] = length;
                    }
                    break;
                case 35:
                    val = _bitReader!.ReadBits(3) + 2;
                    while (val-- > 0)
                    {
                        lengths[i++] = length;
                    }
                    break;
                case 36:
                    val = _bitReader!.ReadBits(6) + 10;
                    while (val-- > 0)
                    {
                        lengths[i++] = length;
                    }
                    break;
                default:
                    length = val + 1;
                    break;
            }
            lengths[i] = length;
        }

        return PrefixCode.FromLengths(lengths, numCodes, 32);
    }

    private int NextLiteralOrMatch(out int offset, out int length)
    {
        offset = 0;
        length = 0;

        int val = _currentCode!.DecodeSymbol(_bitReader!);

        if (val < 0x100)
        {
            // Literal
            _currentCode = _firstCode;
            return val;
        }
        else
        {
            // Match
            _currentCode = _secondCode;

            if (val < 0x13e)
            {
                length = val - 0x100 + 3;
            }
            else if (val == 0x13e)
            {
                length = _bitReader!.ReadBits(10) + 65;
            }
            else if (val == 0x13f)
            {
                length = _bitReader!.ReadBits(15) + 65;
            }
            else
            {
                // End marker (0x140)
                return -1;
            }

            int bitLength = _offsetCode!.DecodeSymbol(_bitReader!);
            if (bitLength == 0)
            {
                offset = 1;
            }
            else if (bitLength == 1)
            {
                offset = 2;
            }
            else
            {
                offset = (1 << (bitLength - 1)) + _bitReader!.ReadBits(bitLength - 1) + 1;
            }

            return 0x100; // Indicate match
        }
    }

